package methods;

public class Class3 {

	public static void main(String[] args) {
		int a=22;
		System.out.println(a&=2);
		System.out.println(a|=2);
		System.out.println(a^=2);
		System.out.println(a>>=2);
		System.out.println(a<<=2);
		
		System.out.println("shift operators");
		System.out.println(20 << 2);
		System.out.println(20 >> 2);
		System.out.println(20>>>2);
		

	}

}
